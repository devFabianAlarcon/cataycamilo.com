<!-- ── COUNTDOWN SCRIPT ──────────────────────────────────── -->
<script>
(function () {
    var target = new Date('2026-09-05T16:00:00');
    function tick() {
        var diff = target - new Date();
        if (diff <= 0) {
            document.getElementById('weddingCountdown').innerHTML =
                '<p class="fw-light fs-4" style="color:var(--cnvs-themecolor); font-family:\'cormorant-garamond\',Georgia,serif;">¡Ya nos casamos! 🎉</p>';
            return;
        }
        var d = Math.floor(diff / 864e5);
        var h = Math.floor((diff % 864e5) / 36e5);
        var m = Math.floor((diff % 36e5) / 6e4);
        var s = Math.floor((diff % 6e4) / 1e3);
        document.getElementById('cd-days').textContent  = d;
        document.getElementById('cd-hours').textContent = String(h).padStart(2,'0');
        document.getElementById('cd-mins').textContent  = String(m).padStart(2,'0');
        document.getElementById('cd-secs').textContent  = String(s).padStart(2,'0');
    }
    tick(); setInterval(tick, 1000);
})();
</script>